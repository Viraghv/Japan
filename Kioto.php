	<h1>Kiotó</h1>
	<p>Kiotó (japánul 京都市 <i>kjótosi</i>, szó szerinti magyar fordításban „főváros”) japán nagyváros Honsú szigetén.
		Lakossága eléri a másfél millió főt. Régebben a császári Japán fővárosa volt, ma a Kiotó prefektúra székhelye,
		amely Oszaka és Kóbe városokkal az Oszaka–Kóbe–Kiotó urbanizált régiót alkotja. 1997 decemberében itt határozták
		el a szén-dioxid és egyéb üvegházhatású gázok kibocsátásával foglalkozó egyezmény létrehozását, ami később
		Kiotói jegyzőkönyv néven vált ismertté.
		1994-ben az UNESCO Kiotó, Udzsi és Ócu városát a Világörökség részévé nyilvánította.</p>
	<h2>Kultúra</h2>
	<h3>Templomok</h3>
	<p>Több mint kétezer templom található a városban, legnagyobb számban a buddhista templomok, mintegy ezerhatszáz
		templom, valamint négyszázra teszik a kisebb- nagyobb sintó szentélyeket, emellett rengeteg palota,
		<i>zen-kert</i>ek és sok új építésű modern épület is megtalálhatók a macshiják mellett. A leghíresebb japán
		templomok itt találhatóak, közülük a legnevezetesebb a <i>Kijomizu-dera</i>, egy csodálatos fából készült
		templom, melyet pillérek támasztanak alá a hegy alján. Itt található a <i>Kinkakudzsi</i> (金閣寺,
		Hepburn-átírással: Kinkakuji, közkedvelt nyugati neve: Aranytemplom, hivatalos neve: Rokuondzsi), a <i>Ginkakudzsi</i>
		(銀閣寺, Hepburn-átírással: Ginkakuji, közkedvelt nyugati neve: Ezüsttemplom, hivatalos neve: Dzsisódzsi), valamint
		a <i>Rjóandzsi</i> is, amely sziklakertjéről lett híres. 1895-ben építették a város keleti részén a <i>Heian-szentély</i>t
		(平安神宮, Hepburn-átírással: Heian jingū), amely egy sintó templom. A szentélyt a város fönnállásának ezerszázadik
		évfordulójára készítették el, valamint az első (<i>Kammu</i>) és utolsónak ott uralkodó (<i>Kómei</i>)
		császárnak szentelték. A császári családhoz három fontos helyszín is kapcsolódik, amelyek a <i>Kiotó Gjoen</i>ben
		találhatóak. A Kiotó Gjoen egy nemzeti park, ide építették újra a Kiotói császári palotát, a <i>Sentó császári
			palotá</i>t, illetve a császároknak évszázadok óta lakóhelyül szolgáló villákat,mint a <i>Kacuna császári
			villá</i>t, amit az ország legszebb épületei közé sorolnak, a <i>Sugaku-in császári villá</i>>t, amely pedig
		gyönyörű kertjéről híres, és a Sennyúdzsi, itt található Sidzsó és Kómei császár sírja is.
		1994-ben az UNESCO Világörökségi listájára 'Az ősi Kiotó történelmi műemlékei' (Kiotó, Udzsi és Ócu városok)
		néven tizenhét templom került föl a listára.</p>
	<h3>Étkezési kultúra</h3>
	<p>Kiotó híres a bőséges és finom ételeiről. Messze található a tengertől, és a buddhista szerzetesek
		vegetarianizmusa hatott az étkezési kultúrára is, ezért egyszerű, főleg zöldségeket tartalmazó ételeket
		készítenek. Egyes éttermek, a hagyományos japán ízvilágot részesítik előnyben, míg napjainkban igen kedveltté
		váltak, az olyan éttermek, amelyek ötvözik a kiotói konyhát a világ más részeiből származó főzési stílusokkal,
		ezek főleg a <i>Gion</i> (祇園), és a <i>Kiotói állomás</i> környékén találhatóak meg. Kiotóban is megtalálhatók a nem helyi
		specialitások, mint a ramen, szusi és az udon is. A főváros központjában található a <i>Nisiki piac</i> (錦市場, Nisiki
		icsiba), szűk utcákból és öt blokkból tevődik össze, itt több száz kis üzlet és étterem található.</p>
	<h3>Gésák</h3>
	<p>Kiotó nevezetes gésa kultúrájáról. A Gion negyed (祇園, Hepburn-átírással: Gion) a legnépszerűbb gésa úticél. A
		Gion negyedet a Jaszaka-szentély keleten, Kamo-folyó nyugaton határolja. Az utcákon rengeteg üzlet, étterem,
		csamisze( 茶店,Hepburn-átírással: chamise, magyarul: teaház) található, ahol a gésák (másik elnevezésük a geiko)
		és a maikók (a gésa tanoncok) szórakoztatják a vendégeket.</p>
	<h2>Látnivalók</h2>
	<p>Kiotóban elsősorban a fő látványosságok a buddhista és sintó templomok. Japán nemzeti kincsének 20%-a, és Japán
		kulturális örökségének 14%-a Kiotóban található. Az UNESCO Világörökségének listáján a város tizenhét szentélye
		került föl 1994-ben.</p>
	<h3>Fesztiválok</h3>
	<p>Kiotó nevezetes hagyományos fesztiváljairól, amelyeket több mint ezer éve tartanak meg. Az itt rendezett
		fesztiválók jelentős turisztikai látványosságnak számítanak, mint a küldöldi és a japán lakosság számára. Az
		első macurit (fesztivált) május tizenötödikén tartják, ez az Aoi macuri (葵祭), júliusban tartják a Gion macurit
		(祇園祭), amely egyike Japán három legnagyobb fesztiváljának. Emellett az Obon macurit (augusztus 16.), ennek
		legismertebb része a Gozan no Okuribi (五山送り火) vagy ismertebb nevén Daimonji (大文字), amikor a hegyen lévő
		fáklyákat úgy gyújtják meg, hogy az a <i>dai</i> (大,だい, nagy,hatalmas), írásjegyet mutassa. A tűz azt a cél szolgálja,
		hogy a szellemeket átvezesse a túlvilágra. Október 22-én van a Dzsidai macuri (時代祭), amelyt a Heian-szentélyben
		(平安神宮) tartanak, és Kiotó dicső múltját ünneplik meg.</p>
